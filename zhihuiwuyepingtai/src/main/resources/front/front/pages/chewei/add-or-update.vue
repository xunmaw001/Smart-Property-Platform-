            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			cheweiPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.cheweiPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.cheweiName)) {
                    this.$utils.msg(`车位名称不能为空`);
                    return
                }
				if ((!this.ruleForm.cheweiUuidNumber)) {
                    this.$utils.msg(`车位编号不能为空`);
                    return
                }
				if ((!this.ruleForm.cheweiPhoto)) {
                    this.$utils.msg(`车位照片不能为空`);
                    return
                }
				if ((!this.ruleForm.cheweiAddress)) {
                    this.$utils.msg(`车位地点不能为空`);
                    return
                }
				if ((!this.ruleForm.cheweiZhuangtaiTypes)) {
                    this.$utils.msg(`车位状态不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`chewei`, this.ruleForm);
                } else {
                    await this.$api.save(`chewei`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
