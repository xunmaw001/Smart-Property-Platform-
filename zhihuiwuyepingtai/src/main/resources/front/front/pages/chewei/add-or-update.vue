            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let cheweiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'chewei_types',
            }
			let cheweiTypes = await this.$api.page(`dictionary`, cheweiTypesParams);
			this.cheweiTypesOptions = cheweiTypes.data.list
		/*下拉框*/
			let cheweiZhuangtaiTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'chewei_zhuangtai_types',
            }
			let cheweiZhuangtaiTypes = await this.$api.page(`dictionary`, cheweiZhuangtaiTypesParams);
			this.cheweiZhuangtaiTypesOptions = cheweiZhuangtaiTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`chewei`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.cheweiId){
                this.ruleForm.cheweiId = options.cheweiId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            cheweiTypesChange(e) {
                this.cheweiTypesIndex = e.target.value
                this.ruleForm.cheweiValue = this.cheweiTypesOptions[this.cheweiTypesIndex].indexName
                this.ruleForm.cheweiTypes = this.cheweiTypesOptions[this.cheweiTypesIndex].codeIndex
            },
            cheweiZhuangtaiTypesChange(e) {
                this.cheweiZhuangtaiTypesIndex = e.target.value
                this.ruleForm.cheweiZhuangtaiValue = this.cheweiZhuangtaiTypesOptions[this.cheweiZhuangtaiTypesIndex].indexName
                this.ruleForm.cheweiZhuangtaiTypes = this.cheweiZhuangtaiTypesOptions[this.cheweiZhuangtaiTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
